        Format:

        "KEY" = "CONTENT";

        These key/content pairs function just like an NSDictionary, and convention is to 
        use the default language translation of the content as the key:

        "You like?" = "You like?";
        
        Key/content pairs can also contain format strings:

        "Yesterday you sold %@ apps" = "Yesterday you sold %@ apps";

        "Yesterday you sold %@ apps" = "Ayer le vendi&oacute; %@ aplicaciones";
        "You like?" = "~Es bueno?~";


